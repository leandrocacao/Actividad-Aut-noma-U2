# Solicitar el nombre y la edad del usuario

nombre = input("Ingresa tu Nombre: ")
edad = int(input("Ingresa tu Edad: "))

# Calcular el año en el que el usuario cumplirá 100 años

año_actual = 2024
años_restantes = 100 - edad
año_100 = año_actual + años_restantes

# Imprimir el resultado en pantalla

print(f"Hola {nombre}, cumplirás 100 años en el año {año_100}")





